public class ComparisonOperation {
   public static void main(String[] args) {
    
    //Operasi Perbandingan = Comparison Operation

    int a = 100;
    int b = 100;

    System.out.println(a > b);
    System.out.println(a < b);
    System.out.println(a >= b);
    System.out.println(a <= b);
    System.out.println(a == b);
    System.out.println(a != b);
   } 
}
